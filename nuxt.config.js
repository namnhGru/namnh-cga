import axios from 'axios'
const dynamicRoutes = async () => {
  try {
    const { data } = await axios.get(
      'https://secure-wave-64960.herokuapp.com/posts'
    )
    return data.map((post) => `/writing/${post.slug}`)
  } catch (err) {
    // eslint-disable-next-line no-console
    console.error(err)
  }
}

